# variable and types
a = 10
print (a, type(a)) # int
a = 2.5
print (a, type(a)) # float
a = True
print (a, type(a)) # bool
a = 3 + 4j
print (a, type(a)) # complex
a = "python"
print (a, type(a)) # str
a = [11, 22, 33, 44]
print (a, type(a)) # list - similar to array
a = (11, 22, 33, 44)
print (a, type(a)) # tuple - similar to list; cannot be changed
a = {11, 22, 33, 44}
print (a, type(a)) # set
a = {11 : 22, 33 : 44}
print (a, type(a)) # dict
 


 

